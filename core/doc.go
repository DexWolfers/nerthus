package core

// key value map:
//  number value  is big endian.
//------------------------------------------------------------------------------------------------|
//-+--------+--------------+-------------+------------+-----------------------------+-------------+
// | prefix |  key-p1  	   | key-p2 	 |  key-p3    |        Value     			|             |
//-+--------+--------------+-------------+------------+-----------------------------+-------------+
// | "H"    |  unit hash   |                          |   mc addr ,number		    |             |
//----------+--------------+-------------+------------+-----------------------------+-------------+
// | "h"    |  mc address  |  number	 |  "n"       |   unit hash                 |             |
//----------+--------------+-------------+------------+-----------------------------+-------------+
// | "h"    |  mc address  |  number     |  unit hash |   *header                   |             |
//----------+--------------+-------------+------------+-----------------------------+-------------+
// | "b"    |  mc address  |  number     |  unit hash |   *body[tx+votes]           |             |
//----------+--------------+-------------+------------+-----------------------------+-------------+
// |"hbro"  |  mc address  |  number	 |        |   [unit hash1,unit hash2,...]   |兄弟单元哈希集合|
//----------+--------------+-------------+------------+-----------------------------+-------------+
// | "LastUnit"  |  mc address  |                     |  unit hash               |账户链最后一个单元|
//----------+--------------+-------------+------------+-----------------------------+-----------+--
// | "r"    |  mc address  |  number     |  unit hash |  *receipts   			    |  交易凭据    |
//----------+--------------+-------------+------------+-----------------------------+----------+---
// | "l"    |  tx Hash     |                          |unit hash,unit number,tx index|  交易凭据    |
//----------+--------------+-------------+------------+-------------+---------------+-----------+--
// |  ----------单元引用关系存储-----------                                                         |
//----------+--------------+-------------+------------+-------------+---------------+-------------+
// | "uc_"  |  unit hash   |  "_c"                    | children count              | 子单元数     |
//----------+--------------+-------------+------------+-------------+---------------+-------------+
// | "uc_"  |  unit hash   |  index      |            |  unit hash                  | 子单元       |
//----------+--------------+-------------+------------+-------------+---------------+-------------+
// | "txs"  | tx hash      |             |            |  [{action，unit},...]       | 交易执行阶段  |
//----------+--------------+-------------+------------+-------------+---------------+-------------+
// | "lw"   |  mc address  |             |            |  unit hash                  | 见证人列表    |
//----------+--------------+-------------+------------+-------------+---------------+-------------+
// | "wb"   |  mc address  |             |            |  number                | 见证人列表起始位置  |
//----------+--------------+-------------+------------+-------------+---------------+-------------+
// | "we"   |  mc address  |             |            |  number                | 见证人列表结束位置  |
//----------+--------------+-------------+------------+-------------+---------------+-------------+
//----------+--------------+-------------+------------+-------------+---------------+-----------+--

//------------------------------------------------------------------------------------------------+
// |      见证人存储                                                                               |
// +--------+--------------+-------------+------------+-------------------------------------------+
// |    -   |"WitnessLib"  |             |            | capacity, size                            |
// +--------+--------------+-------------+------------+-------------------------------------------+
// |  "wi"  | index        |             |            | address                                   |
// +--------+--------------+-------------+------------+-------------------------------------------+
// |  "wa"  | address      |             |            | address, apply unit hash, cancel unit hash|
// +--------+--------------+-------------+------------+-------------------------------------------+
// 备注：
//    index 为capacity值 只有申请时才会添加这条记录
//    申请  capacity + 1, size + 1, applyHash: 申请单元hash
//    注销  size - 1, cancelHash: 注销单元hash

// 见证人被引用信息
//------------------------------------------------------------------------------------------------+
// |      见证人引用关系                                                                            |
// +----------+--------------+-------------+------------+-------------------------------------------+
// +----------+--------------+-------------+------------+-------------------------------------------+
// |"wref"    | wit_address  |             |            | capacity, size                            |
// +----------+--------------+-------------+------------+-------------------------------------------+
// |"wref_idx"| wit_address  | index       |            | address                                 |
// +----------+--------------+-------------+------------+-------------------------------------------+
// |"wref_inf"| wit_address  | address     |            | address, BindHash, UnbindHash
// +----------+--------------+-------------+------------+-------------------------------------------+
// 备注:
//    绑定见证人关系  BindHash: 更换单元Hash
//    解绑见证人关系  UnbindHash: 更换单元Hash

// 见证人见证交易信息
//--------------------------------------------------------------------------------------------------+
// |      用户见证人关系                                                                          |
// +--------+--------------+-------------+------------+-------------------------------------------+
// +--------+----------+----------+---------+-------------------------------------------+
// |"rw"    | address  |          | "c"     |                        |
// +--------+----------+----------+---------+------------+--------------------------------- ---+
// |"rw"    | address  | index    |         |                                 |
// +--------+----------+----------+---------+------------+-------------------------------------------+
// |"rw"    | address  | hash     | "s"     |  存储见证人
// +--------+----------+----------+---------+------------+-------------------------------------------+
// |"rw"    | address  | hash     | "b"     |  开始高度
// +--------+----------+----------+---------+------------+-------------------------------------------+
// |"rw"    | address  | hash     | "e"     |  结束高度
// +--------+----------+----------+---------+------------+-------------------------------------------+
// 备注:
// rw + address + c ==> count
// rw + address + index  ==> hash
// rw + address + hash + s ==> witnesses
// rw + address + hash + b ==> number
// rw + address + hash + e ==> number
